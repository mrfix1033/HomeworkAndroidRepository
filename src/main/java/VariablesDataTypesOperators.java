public class VariablesDataTypesOperators {

    public static void main(String[] args) {
        int integer = 14;
        double double_number = 1.4;
        char c = 61;
        double result = integer + double_number + c;
        System.out.println(result);

        int a = 5, b = 12;
        boolean check = a < b;
        System.out.println(check);

        a = 6;
        a += 6;
        a -= 2;
        a /= 5;
        a *= 7;
    }
}

public class Calculator {
    public double a, b;

    public double sum(double a, double b) {
        return a + b;
    }

    public double sub(double a, double b) {
        return a - b;
    }

    public void mul(double a, double b) {
        System.out.println(a * b);
    }

    public void div(double a, double b) {
        System.out.println(a / b);
    }
}
